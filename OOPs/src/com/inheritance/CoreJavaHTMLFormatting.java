package com.inheritance;

public class CoreJavaHTMLFormatting {
    public static void main(String[] args) {
        // Simulate HTML-style formatting in console output
        System.out.println("<html>");
        System.out.println("<head>");
        System.out.println("<title>Java Formatted Output</title>");
        System.out.println("</head>");
        System.out.println("<body>");

        // Display content with formatting
        System.out.println("<h1>This is a Heading</h1>");
        System.out.println("<p>This is a paragraph of text.</p>");
        System.out.println("<ul>");
        System.out.println("<li>Item 1</li>");
        System.out.println("<li>Item 2</li>");
        System.out.println("<li>Item 3</li>");
        System.out.println("</ul>");

        // End HTML-style formatting
        System.out.println("</body>");
        System.out.println("</html>");
    }
}
