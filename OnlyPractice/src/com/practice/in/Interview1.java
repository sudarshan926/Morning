package com.practice.in;

public class Interview1 {

	public static void main(String[] args) {
		// 1 week =7 days
		int days = 7;
		String Rainbowcolor;
		switch (days) {
		case 1:
			Rainbowcolor = "Red";
			System.out.println("color :" + Rainbowcolor);
			break;

		case 2:
			Rainbowcolor = "Orange";
			System.out.println("color :" + Rainbowcolor);
			break;
		case 3:
			Rainbowcolor = "Yellow";
			System.out.println("color :" + Rainbowcolor);
			break;
		case 4:
			Rainbowcolor = "Blue";
			System.out.println("color :" + Rainbowcolor);
			break;
		case 5:
			Rainbowcolor = "Indigo";
			System.out.println("color :" + Rainbowcolor);
			break;
		case 6:
			Rainbowcolor = "Violet";
			System.out.println("color :" + Rainbowcolor);
			break;
		case 7:
			Rainbowcolor = "Green";
			System.out.println("color :" + Rainbowcolor);
			break;
		default:
			System.out.println("Not Match any Rainbow Color ");
		}	}
}
